str="what we think we become; we are Python programmer"
# islower() :method of builtins.str instance
#     Return True if the string is a lowercase string, False otherwise.
#
#     A string is lowercase if all cased characters in the string are lowercase and
#     there is at least one cased character in the string.
print(str.islower()) #False

# isupper() method of builtins.str instance
#     Return True if the string is an uppercase string, False otherwise.
#
#     A string is uppercase if all cased characters in the string are uppercase and
#     there is at least one cased character in the string.
print(str.isupper()) #False

print("hi how are u!".islower()) #True
print("HI SUP?".isupper()) #True



